    private void btn_salvar_musicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvar_musicaActionPerformed
        // TODO add your handling code here:
        if((TextField_nome.getText().equals(""))||(ComboBox_gen1.getSelectedIndex() == 0)){
            
            JOptionPane.showMessageDialog(null,"Opa!, insira mais dados...","Opss!!", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Musica m = new Musica();
            Genero g = new Genero();
            MusicaDAO dao = new MusicaDAO();
            m.setMusica(TextField_nome.getText());
            if(ComboBox_gen1.getSelectedItem().toString().equals("Outros")){
                m.setGenero1(TextField_outros.getText());
                g.setUsuario(Text_usuario.getText());
                g.setGenero(TextField_outros.getText());
                dao.creategenero(g);
            }
            else{
                m.setGenero1(ComboBox_gen1.getSelectedItem().toString());
                g.setUsuario(Text_usuario.getText());
                g.setGenero(ComboBox_gen1.getSelectedItem().toString());
                dao.creategenero(g);
            }
            if(ComboBox_gen2.getSelectedIndex()== 0){
                m.setGenero2(ComboBox_gen1.getSelectedItem().toString());
                g.setUsuario(Text_usuario.getText());
                g.setGenero(ComboBox_gen1.getSelectedItem().toString());
                dao.creategenero(g);
            }
            else{
                m.setGenero2(ComboBox_gen2.getSelectedItem().toString());
                g.setUsuario(Text_usuario.getText());
                g.setGenero(ComboBox_gen2.getSelectedItem().toString());
                dao.creategenero(g);
            }
            m.setUsuario(Text_usuario.getText());
            m.setNota(0);
            dao.create(m);
            dao.limpagenero();
        }
        LoadComboBoxGen();
        LoadTabelaAvaliacao();
        
    }//GEN-LAST:event_btn_salvar_musicaActionPerformed

