    private void btn_add_genActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_genActionPerformed
        // TODO add your handling code here:
        if(ComboBox_generos.getSelectedIndex()!=0){
            Genero gen = new Genero();
            MusicaDAO dao = new MusicaDAO();
            gen.setUsuario(Text_usuario.getText());
            gen.setGenero(ComboBox_generos.getSelectedItem().toString());
            dao.createTbGenero(gen);
            ComboBox_generos.setEnabled(false);
            btn_add_gen.setEnabled(false);
            btn_cancelar_gen.setEnabled(false);
            LoadTabelaGen();
            dao.limpaGeneroPreferido();
        }
        else{
            JOptionPane.showMessageDialog(null,"Selecione um gênero...","Opss!!", JOptionPane.WARNING_MESSAGE);
        }
        ManipulacaoInterfase("Navegar");
    }//GEN-LAST:event_btn_add_genActionPerformed

