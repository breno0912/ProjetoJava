    public void LoadComboBoxGen(){
        MusicaDAO dao = new MusicaDAO();
        dao.limpagenero();
        LoadTabelaGen();
        
        for(Genero g: dao.readgen()){
            ComboBox_generos.addItem(g.toString());
            ComboBox_recomendacao.addItem(g.toString());
        }
    }
    
    
    public void LoadTabelaGen(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{""},0);
        MusicaDAO dao = new MusicaDAO();
        for(GeneroPreferido ng: dao.readGenPref(Text_usuario.getText())){
            modelo.addRow(new Object[]{
                ng.getGenero()
            });
        }
            
        Tabela_genero.setModel(modelo);
    }
    
    
    
    public void LoadTabelaRecomendada(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Música","Nota"},0);
        MusicaDAO dao = new MusicaDAO();
        
        for(Musica i: dao.readRecomendadas(ComboBox_recomendacao.getSelectedItem().toString(),Text_usuario.getText())){
            
            modelo.addRow(new Object[]{
                i.getMusica(),
                i.getNota()
            });
        }
        
        Tabela_recomendados.setModel(modelo);
    }
    
     public void LoadTabelaAvaliacao(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"Músicas", "Notas"},0);
        MusicaDAO dao = new MusicaDAO();
        
        for(Musica i: dao.readAvaliacao(Text_usuario.getText())){
            
            modelo.addRow(new Object[]{
                i.getMusica(),
                i.getNota()
            });
        }
        
        Tabela_Avaliacao.setModel(modelo);
    }
    
    public void ManipulacaoInterfase (String modo){
        switch(modo){
            case "Navegar":
                    ComboBox_generos.setEnabled(false);
                    btn_add_gen.setEnabled(false);
                    btn_cancelar_gen.setEnabled(false);
                    btn_excluir_gen.setEnabled(false);
                    btn_novo_gen.setEnabled(true);
                    ComboBox_generos.setSelectedIndex(0);
                break;
            case "Novo":
                    ComboBox_generos.setEnabled(true);
                    btn_add_gen.setEnabled(true);
                    btn_cancelar_gen.setEnabled(true);
                    btn_excluir_gen.setEnabled(false);
                    btn_novo_gen.setEnabled(false);
                break;
            case "Excluir":
                    ComboBox_generos.setEnabled(false);
                    btn_add_gen.setEnabled(false);
                    btn_cancelar_gen.setEnabled(false);
                    btn_excluir_gen.setEnabled(false);
                    btn_novo_gen.setEnabled(true);
                    btn_excluir_gen.setEnabled(true);
                break;
            case "Cancelar":
                    ComboBox_generos.setEnabled(false);
                    btn_add_gen.setEnabled(false);
                    btn_cancelar_gen.setEnabled(false);
                    btn_novo_gen.setEnabled(true);
                    
                break;
            default: System.out.println("Modo Invalido...");
        }
    }

