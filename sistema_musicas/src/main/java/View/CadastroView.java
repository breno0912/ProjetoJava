    private void btn_concluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_concluirActionPerformed
        // TODO add your handling code here:
        String usuarioTexto = Texto_n_usuario.getText();
        String senhaTexto = new String(PasswordField_senha.getPassword());
        
        LoginDAO dao = new LoginDAO();
        Login l = new Login(usuarioTexto, senhaTexto);
        
        l.setUsuario(usuarioTexto);
        l.setSenha(senhaTexto);
        dao.cadastro(l); 
        
        LoginView lv = new LoginView();
        lv.setVisible(true);
        this.dispose();
        
        
        
        
    }//GEN-LAST:event_btn_concluirActionPerformed

